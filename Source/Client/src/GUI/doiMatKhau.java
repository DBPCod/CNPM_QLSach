    private void setUpTK(String MaTK)
    {
        JSONObject json = new JSONObject(client1.getList("ListTaiKhoan"));
        JSONArray jsonArray = json.getJSONArray("list");
        for(int i=0;i<jsonArray.length();i++)
        {
            
            JSONObject nvObject = jsonArray.getJSONObject(i);
              String chuoi = nvObject.getString("maTK");
            if(chuoi.equalsIgnoreCase(MaTK))
            {
                tenTK.setText(nvObject.getString("tenTK"));
                mkHienTai.setText(nvObject.getString("matKhauTK"));
                return;
            }
        }
    }
    
    
