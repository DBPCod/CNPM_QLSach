    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        String tenTK1 = tenTK.getText();
        if(!mkMoi.getText().equals(mkMoi1.getText()))
        {
            JOptionPane.showMessageDialog(null, "Mật khẩu chưa trùng khớp!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String matkhau = mkMoi.getText();
        
        if (matkhau.length() < 6) {
            JOptionPane.showMessageDialog(null, "Mật khẩu mới phải có ít nhất 6 ký tự!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        JSONObject json = new JSONObject();
        json.put("method","UPDATETK");
        json.put("MaTK",MaTK1);
        json.put("TenTK",tenTK1);
        json.put("MatkhauTK",matkhau);
        JSONObject json1 = new JSONObject(client1.suaDT(json.toString()));
        if(json1.getString("ketqua").equals("true"))
        {
            JOptionPane.showMessageDialog(null, "Sửa thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
        }
        else 
        {
            JOptionPane.showMessageDialog(null, "Sửa không thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }    
    }//GEN-LAST:event_jButton1MouseClicked

